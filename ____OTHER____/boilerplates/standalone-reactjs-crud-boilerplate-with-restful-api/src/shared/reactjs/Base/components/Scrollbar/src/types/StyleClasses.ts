/**
 * Interface representing CSS classes for scrollbar components.
 * @interface
 */
export interface StyleClasses {
  /** CSS class for the container element. */
  container: string;
  /** CSS class for the content element. */
  content: string;
  /** CSS class for the horizontal track element. */
  trackHorizontal: string;
  /** CSS class for the horizontal thumb element. */
  thumbHorizontal: string;
  /** CSS class for the vertical track element. */
  trackVertical: string;
  /** CSS class for the vertical thumb element. */
  thumbVertical: string;
}
